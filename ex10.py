# These are escapes sequences

tabby_cat = "\tI'm tabbed in." # \t adds an indentation\horizontal tab within the string
persian_cat = "I'm split\non a line." #\n starts the string in a new line
backslash_cat = "I'm \\ a \\ cat."

# allows you to put as many lines of texts or characters as you want
fat_cat = """ 
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)